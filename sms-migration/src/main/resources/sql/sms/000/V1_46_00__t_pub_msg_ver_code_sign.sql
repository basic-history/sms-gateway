COMMENT ON COLUMN PUB_MSG_VER_CODE_SIGN.SIGN_CODE IS '平台签名编号';
COMMENT ON COLUMN PUB_MSG_VER_CODE_SIGN.CHANNEL_CODE IS '通道编号';
COMMENT ON COLUMN PUB_MSG_VER_CODE_SIGN.APP_CODE IS '应用名称';
COMMENT ON COLUMN PUB_MSG_VER_CODE_SIGN.CREATE_TIME IS '创建时间';
COMMENT ON COLUMN PUB_MSG_VER_CODE_SIGN.CREATE_BY IS '创建人';
COMMENT ON COLUMN PUB_MSG_VER_CODE_SIGN.REMARK IS '备注';