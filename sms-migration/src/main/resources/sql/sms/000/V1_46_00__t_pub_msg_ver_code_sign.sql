CREATE TABLE PUB_MSG_VER_CODE_SIGN 
(
  ID VARCHAR2(32 CHAR) NOT NULL ,
  VER_CODE VARCHAR2(32 CHAR) NOT NULL ,
  SIGN_CODE VARCHAR2(6 CHAR) NOT NULL ,
  CHANNEL_CODE VARCHAR2(2 CHAR) NOT NULL ,
  APP_CODE VARCHAR2(4 CHAR) NOT NULL ,
  CREATE_TIME TIMESTAMP(6)  NOT NULL ,
  CREATE_BY VARCHAR2(30 CHAR) NOT NULL ,
  REMARK VARCHAR2(255 CHAR) NULL 
);
